let mood = "hungry";

switch (mood) {
  case "happy":
    console.log("Keep smiling 😄");
    break;
  case "sad":
    console.log("Cheer up 🌈");
    break;
  case "hungry":
    console.log("Grab some snacks 🍕");
    break;
  case "tired":
    console.log("Take a nap 😴");
    break;
  default:
    console.log("Just keep coding 💻");
}
